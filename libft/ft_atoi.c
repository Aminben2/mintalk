#include "libft.h"

int	ft_atoi(const char *nptr)
{
	unsigned long	number;
	int				i;
	int				sign;

	number = 0;
	sign = 1;
	i = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		number = number * 10 + (nptr[i] - '0');
		if (number > 9223372036854775807 && sign == 1)
			return (0);
		if (number > 9223372036854775807 && sign == -1)
			return (-1);
		i++;
	}
	return (number * sign);
}
